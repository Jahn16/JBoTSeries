class Song():

    def set_url(self,url):
        self.url = url

    def get_url(self):
        return self.url

    def set_title(self,title):
        self.title = title

    def get_title(self):
        return self.title

    def set_author(self,author):
        self.author = author

    def get_author(self):
        return self.author